  <h4>Pronunciation matters - believe me!</h4>
  <p>Why?</p>
  <ol>
    <li>Rightly or wrongly, the way that you speak is one of the first things that people 
      notice about you. People will judge your background, 
      education and social standing just by the way you speak. </li>
    <li>Even if you can already communicate in English if you don't 
      speak clearly, people won't understand you and they won't want 
      to talk to you, because it's just too tiring.</li>
    <li>Listening and understanding are also important. If you don't 
      know how different words are pronounced you will struggle to improve your 
      understanding. </li>
  </ol>
  <p>Don't confuse pronunciation with accent. Good pronunciation doesn't mean that we all have to talk like the Queen of England. 
    As long as your pronunciation is understandable and pleasant that's 
    fine, if people struggle to understand you then you may need to try and reduce your accent, but unless you are an actor, and you need to play different roles, or you want to become a spy, it's not worth trying to get rid of your accent. </p>
  <p>That said, successful speaking is more than just knowing how to pronounce words correctly.</p>
  <p>Enunciation is is the act of speaking clearly and concisely,  making each word sound as clear as possible, without sounding as if you think the person you are talking to is an idiot.</p>
  <p>Elocution is about  putting the right emphasis and nuance into what you are saying. Even kids born and bred in the UK take elocution lessons. </p>
