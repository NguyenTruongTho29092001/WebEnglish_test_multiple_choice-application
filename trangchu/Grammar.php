        <p>Grammar is the mortar that holds the bricks of vocabulary together. Without good mortar bricks can come tumbling down and that can cause embarrassing misunderstandings.  There is a common impression that learning English grammar is painful, but it is a lot easier than many other languages. Of course, English is more than just memorizing grammar rules, the grammar must be incorporated into your everyday use of the language. Theory and practice should always go hand in hand.      <a href="/grammar/grammarmatters.html" title="Grammar matters">Grammar matters</a>.</p>
        <p>That said, here on the Learn English Network, we believe that when you begin to learn English, speaking and building up your vocabulary is more important than worrying about correct grammar. Then, as you progress, you will incorporate more grammar into your learning programme.</p>
