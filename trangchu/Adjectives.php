<p>When we compare  things, people or even ideas we look at what makes them different 
        from each other.</p>
<p>Comparative adjectives are used to show what quality one thing has more 
        or less than the other. They normally come before any other adjectives. </p>
<p><span class="standout">!Tip</span> - It's a good idea to learn opposites (antonyms) when learning adjectives.    </p>
<p><em>For example: </em></p>
  <div class="tacenter">
    <h3>Tall / Short</h3>
    <p><img src="../images/vocab/adjectives/Tall.gif" alt="tall" width="32" height="72"><img src="../images/vocab/adjectives/Short.gif" alt="short" width="23" height="38" align="bottom"></p>
</div>
  <div class="tacenter">The man on the left is <strong>taller </strong>than the man on the right. <br>
    The man on the right is <strong>shorter </strong>than the man on the left. </div>
      
     <p>&nbsp;</p>
     <p><span class="standout">!Note </span>- Have you noticed that when we are comparing two things like this we put <em><b>than</b></em> between the adjective and the thing being compared.         </p>
     <div class="tacenter">
             <h3>Fast / Slow</h3>
             <p><img src="../images/vocab/transport/car_s.gif" alt="car" width="80" height="80"> <img src="../images/vocab/transport/bicycle_s.gif" alt="bicycle" width="80" height="80"></p>
             </div>

     <div class="tacenter">A car is <strong>faster <em>than</em></strong> a bicycle. <br>
       A bicycle is<strong> slower <em>than </em></strong>a car. </div>
     <p><em>For example: </em></p>
 <div class="tacenter">
   <h3>Big / Small</h3>
   <p><img src="../images/adjectives/bluebag.gif" alt="blue bag" width="60" height="60">
     <img src="../images/adjectives/redbag.gif" alt="red bag" width="80" height="80"></p>
 </div>
<div class="tacenter">The red bag is <strong>bigger than</strong> the blue bag. <br>
The blue bag is <strong>smaller than</strong> the red bag. </div>

              <h3>Forming the comparative </h3>
              <p>The way you form the comparative from an adjective depends â€“ on the number of&nbsp;syllables&nbsp;and&nbsp;the spelling of the adjective.      </p>
              <table width="75%" border="1" class="tacenter">
                      <tbody><tr>
      <th>Form</th>
      <th>Rule</th>
      <th>For example </th>
    </tr>
    <tr>
      <td>Words of one syllable ending in 'e'. </td>
      <td><div class="tacenter"> Add -r to the end of the word.</div></td>
      <td><p>wide - wider<br>
      </p></td>
    </tr>
    <tr>
            <td colspan="3"><p><em>For example:-</em></p>
                    <p>A motorway is <strong>wider than</strong> a road.</p>
                    <p>&nbsp;</p></td>
            </tr>
    <tr>
      <td>Words of one syllable, with one vowel and one consonant at the end. </td>
      <td>Double the consonant and add -er to the end of the word. </td>
      <td><p>big - bigger</p></td>
    </tr>
    <tr>
            <td colspan="3"><em>For example:-</em><img src="../images/cartoons/leunig-bigger-smaller.JPG" alt="bigger vs smaller"><span class="btn-mini"><a href="http://www.leunig.com.au/">http://www.leunig.com.au/ - Reproduced with permission. </a></span></td>
            </tr>
    <tr>
      <td>Words of one syllable, with more than one vowel or more than  one consonant at the end.</td>
      <td>Add - er to the end of the word. </td>
      <td>high - higher </td>
    </tr>
    <tr>
            <td colspan="3">Mount Everest is <strong>higher than</strong> Mount Snowdon.</td>
            </tr>
    <tr>
      <td>Words of two syllables, ending in 'y'. </td>
      <td>Change 'y' to 'i', and add -er to the end of the word. </td>
      <td>happy - happier</td>
    </tr>
    <tr>
            <td colspan="3"><p><em>For example:-</em></p>
                    <p>Some people seem to be<strong> luckier than</strong> other people.</p></td>
            </tr>
    <tr>
      <td>Words of two syllables or more, not ending in 'y'. </td>
      <td>Place 'more' before the adjective.</td>
      <td>beautiful - more beautiful </td>
    </tr>
    <tr>
            <td colspan="3"><p><em>For example:-</em></p>
                    <p>Arguably Rome is<strong> more beautiful than</strong> Paris. </p></td>
            </tr>
  </tbody></table>
  <p>&nbsp;</p>
  <p><span class="btn-danger">!Warning </span>- For every rule, there is at least one exception and there are also irregular adjectives that you need to memorise as you come across them. </p>
  <p><em>For example:-</em></p>
  <ul>
    <li>'good' becomes 'better' </li>
    <li> 'bad' becomes 'worse'</li>
    <li> 'far' becomes 'farther' or 'further'      </li>
  </ul>
