                              <h2 class="standout">How to use these pages:-</h2>
                              <p>Double click on any word for its definitions, and to listen to the pronunciation. </p>
                                <h4><span class="standout">Beginners:</span> </h4>
                                <ul>
                                        <li>Learn the basic words at the beginning of each theme.</li>
                                        <li>Do the vocabulary tests.</li>
                                        <li>Search for different images on Google Images and make a collection of links for each word.</li>
                                        <li>Review the words regularly.</li>
                                </ul>

                                        <h4><span class="standout">Intermediate Learners:</span></h4>
                                        <ul>
                                                <li>Make sure you know the words at the beginning of each theme.</li>
                                                <li>Study the build-up section and dialogues at the end of each section. </li>
                                                <li>You should be able to do the games and tests.</li>
                                                <li>Make up your own dialogues (you can share them with us on the forum, and we will correct them). </li>
                                                <li>Go to the expanded themes page.</li>
                                                </ul>
                                        <h4><span class="standout">Advanced Learners:</span></h4>
                                        <ul>
                                                <li>You should already know all the words at the beginning of each theme.</li>
                                                <li>You should be able to understand the build-up section and dialogues at the end of each section.</li>
                                                <li>You should be able to easily complete the games and tests.</li>
                                                <li>You should be able to comfortably read the culture articles for each section.</li>
                                                <li>Why not write your own guide to the culture in your country for each topic (you can share it with us on the forum if you like).</li>

                                        </ul>
